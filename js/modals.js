//Modal 1
const toggleModal = () => {
    document.querySelector('.modal')
    .classList.toggle('modal--hidden');

};
document.querySelector('#show-modal')
.addEventListener('click', toggleModal);

document.querySelector('.modal_close_bar')
.addEventListener('click',toggleModal);

